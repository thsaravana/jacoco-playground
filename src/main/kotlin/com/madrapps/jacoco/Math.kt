package com.madrapps.jacoco

class Arithmetic {

    fun add(a: Int, b: Int): Int {
        return a + b
    }

    fun subtract(a: Int, b: Int): Int {
        return a - b
    }

    fun multiply(a: Int, b: Int): Int {
        return a * b
    }

    fun divide(a: Int, b: Int): Int {
        return a / b
    }

    fun modulo(a: Int, b: Int): Int {
        return a % b
    }

//    fun area(a: Int, b: Int): Int {
